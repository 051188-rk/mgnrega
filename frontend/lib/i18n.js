/**
 * Translates a key based on the selected language.
 * Falls back to English if the key or language is missing.
 */
export function t(key, lang = 'en') {
  // Try to get the translation from the specified language
  if (dict[lang] && dict[lang][key]) {
    return dict[lang][key];
  }
  // Fall back to English if the translation is missing
  if (dict.en[key]) {
    return dict.en[key];
  }
  // Return the key itself as a last resort
  return key;
}

/**
 * Provides a localized relative time string.
 * This is a simplified version and can be expanded with a full date-fns or similar library.
 * (Supports 'en' and 'hi' for demonstration)
 */
export function relativeTimeLocalized(date, lang = 'en') {
  if (!date) return '';
  try {
    const d = new Date(date);
    const diff = Date.now() - d.getTime();
    
    const minutes = Math.floor(diff / 60000);
    if (minutes < 1) return lang === 'hi' ? 'अभी' : 'just now';
    if (minutes < 60) {
      return lang === 'hi' ? `${minutes} मिनट पहले` : `${minutes} min ago`;
    }

    const hours = Math.floor(diff / 3600000);
    if (hours < 24) {
      return lang === 'hi' ? `${hours} घंटे पहले` : `${hours} hours ago`;
    }

    const days = Math.floor(hours / 24);
    return lang === 'hi' ? `${days} दिन पहले` : `${days} days ago`;

  } catch (error) {
    console.error("Error formatting relative time:", error);
    return date; // Return original date string if parsing fails
  }
}